n1 = int(input('Digite um número: '))
n2 = int(input('Digite outro número: '))
nsom = n1+n2
ndiv = n1/n2
nsub = n1-n2
nmult = n1*n2
npot = n1**n2
nquo = n1//n2
nres = n1%n2
print(f'A soma entre {n1} e {n2} é {nsom}')
print(f'A divisão entre {n1} e {n2} é {ndiv:.3f}')
print(f'A subtração entre {n1} e {n2} é {nsub}')
print(f'A multiplicação entre {n1} e {n2} é {nmult}')
print(f'A potenciação entre {n1} e {n2} é {npot}')
print(f'O quociente entre {n1} e {n2} é {nquo}')
print(f'O resto da divisão entre {n1} e {n2} é {nres}')
